namespace openfl.display3D
{
	/**
		Defines the values to use for specifying whether a shader program is a fragment
		or a vertex program.
	**/
	export enum Context3DProgramType
	{
		/**
			A fragment (or pixel) program.
		**/
		FRAGMENT = "fragment",

		/**
			A vertex program.
		**/
		VERTEX = "vertex"
	}
}

export default openfl.display3D.Context3DProgramType;
